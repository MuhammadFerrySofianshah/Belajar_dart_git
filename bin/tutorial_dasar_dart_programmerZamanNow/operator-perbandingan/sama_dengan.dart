void main() {
  // perbandingan sama dengan "=="

  // perbandingan String
  var vNama1 = 'salya';
  var vNama2 = 'ferry';

  if (vNama1 == vNama2) {
    print('$vNama1 dan $vNama2 sama');
  } else {
    print('$vNama1 dan $vNama2 tidak sama');
  }

  // perbandingan bool
  bool vPerbandingan = (5 == 5);
  print(vPerbandingan);
}
