void main() {
  var vAngkaPertama = 11;
  var vAngkaKedua = 2;

  print(vAngkaPertama + vAngkaKedua);
  print(vAngkaPertama - vAngkaKedua);
  print(vAngkaPertama * vAngkaKedua);
  print(vAngkaPertama / vAngkaKedua); // pembagian. hasilnya double
  print(vAngkaPertama ~/ vAngkaKedua); // pembagian. hasilnya int
  print(vAngkaPertama % vAngkaKedua); // sisa bagi
 
}
